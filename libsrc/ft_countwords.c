#include "libft.h"

size_t		ft_countwords(char const *s)
{
	size_t	k;
	size_t	i;

	k = 0;
	i = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		while (s[i] && ((s[i] >= 9 && s[i] <= 13) || s[i] == 32))
			i++;
		if (s[i])
			k++;
		while (s[i] && ((s[i] < 9 && s[i] > 13) || s[i] != 32))
			i++;
	}
	return (k);
}
