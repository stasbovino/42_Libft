#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*a;
	char	*b;

	b = NULL;
	a = (char*)s;
	while (*a)
	{
		if (*a == (char)c)
			b = a;
		a++;
	}
	if (c == '\0')
		return (a);
	return (b);
}
