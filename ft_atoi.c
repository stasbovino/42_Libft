#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t	i;
	size_t	nb;
	int		znak;

	i = 0;
	nb = 0;
	znak = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			znak = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * znak);
}
