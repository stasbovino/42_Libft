#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*lul;

	lul = (unsigned char*)s;
	i = 0;
	while (i < n)
	{
		lul[i] = 0;
		i++;
	}
}
